public class Chap_2_JavaPrint {
    public static void main(String[] args) {
        // Print Strings: 
        System.out.println("Hello World!");
        System.out.print("Hello World! \n");
        System.out.format("Hello World! \n");
        System.out.printf("Hello World! \n");

        // Print Strings:
        System.out.println("My age is: "+(20+6));
        System.out.print("My age is: "+(13*2)+"\n");
        System.out.format("My age is: %d \n",26);
        System.out.printf("My age is: %d \n",(52/2));
    }
}
