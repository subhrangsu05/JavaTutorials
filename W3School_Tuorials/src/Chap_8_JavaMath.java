public class Chap_8_JavaMath {
    public static void main(String[] args) {
        System.out.println("Max Value is: "+Math.max(5,10));
        System.out.println("Min Value is: "+Math.min(5,10));
        System.out.println("Square Root Value is: "+Math.sqrt(25));
        System.out.println("Exponential Value is: "+Math.pow(5,10));
        System.out.println("Absolute Positive Value is: "+Math.abs(-4.7));
        System.out.println("Random Value is: "+(int) (Math.random()*101));
    }
}
